    private void CekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CekActionPerformed
        
        String Gejala = (String) gejala.getSelectedItem();
        String Pesan = "";
                if (Gejala == "Demam") {
                    Pesan = "Rekomendasi Obat: Paracetamol";
                } else if (Gejala == "Nyeri") {
                    Pesan = "Rekomendasi Obat: Ibuprofen";
                } else if (Gejala == "Asam Lambung") {
                    Pesan = "Rekomendasi Obat: Antasida";
                } else if (Gejala == "Alergi") {
                    Pesan = "Rekomendasi Obat: Cetirizine";
                } else if (Gejala == "Infeksi") {
                    Pesan = "Rekomendasi Obat: Amoxicillin";
                } else if (Gejala == "Diabetes") {
                    Pesan = "Rekomendasi Obat: Metformin";
                } else if (Gejala == "Maag") {
                    Pesan = "Rekomendasi Obat: Omeprazole";
                } else if (Gejala == "Asma") {
                    Pesan = "Rekomendasi Obat: Salbutamol";
                } else if (Gejala == "Radang") {
                    Pesan = "Rekomendasi Obat: Dexamethasone";
                } else if (Gejala == "Diare") {
                    Pesan = "Rekomendasi Obat: Loperamide";
                } else {
                    Pesan = "Pilih gejala dari daftar.";
                }
                
                 JOptionPane.showMessageDialog(null, Pesan, "Rekomendasi Obat", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_CekActionPerformed

